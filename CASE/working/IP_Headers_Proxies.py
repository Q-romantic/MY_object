def str_to_dic(s: str = '', flags: [bool, int] = 0) -> dict:
    import re
    # pattern = '^(.*?):\s{0,}(.*)$'    # 上下等价
    pattern = '^(.*?):\s*(.*)$'
    h = headers
    for line in s.splitlines():
        if line == '' or line.startswith('#'):
            continue
        s = re.sub(pattern, '{\'\\1\': \'\\2\'}', line)
        h.update(eval(s))
        if flags:
            print(re.sub(pattern, "\'\\1\': \'\\2\',", line))
    return h


def get_proxy(n: int = 1):  # 默认循环 1 次
    m = 0
    s1 = set()
    for i in range(n):
        url = 'https://proxypool.scrape.center/random'
        proxy = requests.get(url, headers=headers).text
        proxies = {'http': f'http://{proxy}', 'https': f'http://{proxy}'}
        url_test = 'https://www.baidu.com'
        try:
            response = requests.get(url_test, headers=headers, proxies=proxies, timeout=1)
            if response.status_code == 200:
                print(proxies, '---> 可用')
                s1.add(str(proxies))
                m += 1
        except:
            pass

    print(f'IP可用率：{100 * m / n}%')
    for i in s1:
        with open(filename, mode='a', encoding='utf-8') as f:
            f.write(i)
            f.write('\n')


def get_single(file_name):
    list01 = []
    for i in open(file_name):
        if i in list01:
            continue
        list01.append(i)
    with open('tmp.txt', 'w') as handle:
        handle.writelines(list01)
    os.remove(file_name)
    os.rename('tmp.txt', file_name)


